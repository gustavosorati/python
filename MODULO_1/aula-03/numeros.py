# Tipo inteiro
numero_inteiro = 3
print("Inteiro =", numero_inteiro)

# Real com ponto flutuante
numero_real = 3.14
print("Real com ponto flutuante =", numero_real)

# type() -> Retorna o tipo da variável que estamos trabalhando
print("Tipo da variável inteiro =", type(numero_inteiro))
print("Tipo da variável float =", type(numero_real))

# soma + 
soma = 1 + 1
print("1 + 1 =", soma)

# subtração - 
subtracao = 1 - 1
print("1 - 1 =", subtracao)

# multiplicação *
multiplicacao = 2 * 2
print("2 * 2 =", multiplicacao)

# divisão /
divisao = 10 / 2
print("10 / 2 =", divisao)
print("Tipo da variável do resultado da divisão =", type(divisao))

# divisão inteiro /
divisao = 5 // 2
print("5 // 2 =", divisao)
print("Tipo da variável do resultado da divisão =", type(divisao))

# modulo %
modulo = 10 % 2
print("10 % 2 =", modulo)

# conversão de valores
conversao = int(divisao)
print("Valor em inteiro (conversão) =", conversao)
print("Valor em float (conversão) =", float(soma))


